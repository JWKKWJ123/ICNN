def generate_gif(
    img_npy,
    heatmaps,
    overlay_cmap,
    filename='output.gif',
    delay=0.5,
    step=1
):
    """
    Generate a looping GIF by iterating through z slices of a 3D image.

    Parameters
    ----------
    img_npy : np.ndarray
        3D anatomical image array.
    heatmaps : dict
        Dictionary {model_name: 3D heatmap array}.
    overlay_cmap : str or Colormap
        Colormap for the overlay heatmaps.
    filename : str
        Output GIF filename.
    delay : float
        Delay between frames (seconds).
    step : int
        Step size to skip slices (1 = every slice).
    """
    import io
    import imageio.v2 as imageio
    import matplotlib.pyplot as plt

    frames = []
    max_z = img_npy.shape[2] - 1

    for z_coord in range(0, max_z + 1, step):
        # Create the figure for this z slice without showing it
        fig = plot_transverse_heatmaps_with_coronal(
            img_npy,
            heatmaps,
            z_coord,
            overlay_cmap=overlay_cmap,
            show=False
        )

        # Save to buffer
        buf = io.BytesIO()
        fig.savefig(buf, format='png', bbox_inches='tight', pad_inches=0, dpi=100)
        buf.seek(0)
        frame = imageio.imread(buf)
        frames.append(frame)

        buf.close()
        plt.close(fig)

    # Save all frames as a looping GIF (loop=0 → infinite)
    imageio.mimsave(filename, frames, duration=delay, loop=0)
    print(f"Looping GIF saved as: {filename}")




def plot_transverse_heatmaps_with_coronal(
    img_npy,
    heatmaps,
    z_coord,
    cmap='gray',
    vmin=None,
    vmax=None,
    overlay_cmap='hot',
    overlay_vmin=None,
    overlay_vmax=None,
    max_cols=4,
    wspace=0.15,
    hspace=0.0,
    cbar_fraction=0.035,
    cbar_pad=0.005,
    show=True
):
    """
    Plot transverse (axial) slices with overlay heatmaps and a coronal reference slice.
    For VBM-SVM:
      - remove isolated single voxels (connectivity=1)
      - keep image normalization as-is (data-driven)
      - draw a separate colorbar normalized to p-value [0, 0.05]
    """
    import math
    import numpy as np
    import matplotlib.pyplot as plt
    from matplotlib.gridspec import GridSpec
    from matplotlib import colors as mcolors
    from scipy import ndimage

    num_models = len(heatmaps)
    total_plots = num_models + 1  # +1 for coronal reference

    ncols = min(total_plots, max_cols)
    nrows = math.ceil(total_plots / ncols)

    fig = plt.figure(figsize=(ncols * 3, nrows * 3.6))
    gs = GridSpec(nrows, ncols, figure=fig)
    plt.subplots_adjust(wspace=wspace, hspace=hspace)

    axes = [fig.add_subplot(gs[i // ncols, i % ncols]) for i in range(total_plots)]
    for i in range(total_plots, nrows * ncols):
        fig.add_subplot(gs[i // ncols, i % ncols]).axis('off')

    brain_mask = (img_npy != 0)

    # --- Axial heatmaps ---
    for i, (model_name, heatmap) in enumerate(heatmaps.items()):
        ax = axes[i]
        ax.axis('off')

        # Re-mask to brain
        heatmap_filter = np.where(brain_mask, heatmap, 0)

        # VBM-SVM: remove isolated single voxels
        if model_name == "VBM-SVM":
            overlay_01 = heatmap_filter.copy()
            bin_mask = overlay_01 > 0
            bin_mask_clean = remove_single_voxels(bin_mask, connectivity=1)
            removed = int(bin_mask.sum() - bin_mask_clean.sum())
            #print("removed single-voxel sites:", removed)
            display_overlay = overlay_01 * bin_mask_clean
        else:
            display_overlay = heatmap_filter

        # Data-driven normalization (do not clamp to 0.05)
        if overlay_vmin is None or overlay_vmax is None:
            valid = display_overlay[brain_mask]
            if valid.size > 0 and np.isfinite(valid).any():
                local_vmin = float(np.nanmin(valid))
                local_vmax = float(np.nanmax(valid))
                if (not np.isfinite(local_vmin)) or (not np.isfinite(local_vmax)) or (local_vmin == local_vmax):
                    local_vmin, local_vmax = 0.0, 1.0
            else:
                local_vmin, local_vmax = 0.0, 1.0
        else:
            local_vmin, local_vmax = overlay_vmin, overlay_vmax

