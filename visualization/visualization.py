from scipy.ndimage import gaussian_filter, label, generate_binary_structure
def remove_single_voxels(binary_mask, connectivity=1):
    """
    Remove isolated single-voxel clusters (size < 2).
    connectivity=1 -> 6-connected (3D).
    """
    structure = generate_binary_structure(3, connectivity)
    labeled, n = label(binary_mask, structure=structure)
    if n == 0:
        return binary_mask * 0
    sizes = np.bincount(labeled.ravel())
    keep = sizes >= 2
    keep[0] = False  # background
    clean = keep[labeled]
    return clean.astype(binary_mask.dtype)


