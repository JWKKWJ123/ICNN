def plot_coronal_heatmaps_with_axial(
    img_npy,
    heatmaps,
    y_coord,
    cmap='gray',
    vmin=None,
    vmax=None,
    overlay_cmap='hot',
    overlay_vmin=None,
    overlay_vmax=None,
    max_cols=4,
    reference_z=None,
    wspace=0.15,
    hspace=0.0,
    cbar_fraction=0.035,
    cbar_pad=0.005,
    show=True
):
    """
    Plot coronal slices with overlay heatmaps and an axial reference slice.
    Rotation (90 deg) is kept for display.
    NOTE: y index is assumed to run from posterior (small) to anterior (large).
          The axial reference maps y so that larger y appears higher (toward anterior).

    Hard-coded special case:
      - If model name is "VBM-SVM", its heatmap is Gaussian-smoothed (sigma=1)
        and re-masked to avoid spill outside the brain.
    """
    import math
    import numpy as np
    import matplotlib.pyplot as plt
    from matplotlib.gridspec import GridSpec
    from scipy import ndimage
    from scipy.ndimage import gaussian_filter

    num_models = len(heatmaps)
    total_plots = num_models + 1  # +1 for axial reference

    ncols = min(total_plots, max_cols)
    nrows = math.ceil(total_plots / ncols)

    fig = plt.figure(figsize=(ncols * 3, nrows * 3.6))
    gs = GridSpec(nrows, ncols, figure=fig)
    plt.subplots_adjust(wspace=wspace, hspace=hspace)

    axes = [fig.add_subplot(gs[i // ncols, i % ncols]) for i in range(total_plots)]
    # Hide unused slots
    for i in range(total_plots, nrows * ncols):
        fig.add_subplot(gs[i // ncols, i % ncols]).axis('off')

    if reference_z is None:
        reference_z = img_npy.shape[2] // 2

    brain_mask = (img_npy != 0)

    # ---- Coronal overlays ----
    for i, (model_name, heatmap) in enumerate(heatmaps.items()):
        ax = axes[i]
        ax.axis('off')

        # Hard-coded smoothing for VBM-SVM only
        if model_name == "VBM-SVM":
            hm_proc = gaussian_filter(heatmap, sigma=1, mode='constant', cval=0.0)
        else:
            hm_proc = heatmap

        # Re-apply brain mask after smoothing
        heatmap_filter = np.where(brain_mask, hm_proc, 0)

        # Per-slice vmin/vmax if not provided
        if overlay_vmin is None or overlay_vmax is None:
            valid = heatmap_filter[brain_mask]
            local_vmin = float(valid.min()) if valid.size > 0 else 0.0
            local_vmax = float(valid.max()) if valid.size > 0 else 1.0
        else:
            local_vmin, local_vmax = overlay_vmin, overlay_vmax

        # Coronal slice at y = y_coord: shape (x, z)
        coronal_base = img_npy[:, y_coord, :]
        coronal_overlay = heatmap_filter[:, y_coord, :]

        # Keep 90° rotation for display; keep matplotlib default origin='upper'
        base_rot = ndimage.rotate(coronal_base, 90)
        overlay_rot = ndimage.rotate(coronal_overlay, 90)

        ax.imshow(base_rot, vmin=vmin, vmax=vmax, cmap=cmap, origin='upper')
        ax.text(0.03, 0.97, f'{model_name}\n(y={y_coord})', color='white',
                ha='left', va='top', transform=ax.transAxes)

        im = ax.imshow(overlay_rot, cmap=overlay_cmap,
                       vmin=local_vmin, vmax=local_vmax, alpha=0.7, origin='upper')

        cbar = fig.colorbar(im, ax=ax, fraction=cbar_fraction, pad=cbar_pad)
        cbar.ax.tick_params(labelsize=7)

    # ---- Axial reference (z = reference_z) ----
    ax_ref = axes[-1]
    ax_ref.axis('off')

    # Axial slice: shape (x, y)
    axial_base = img_npy[:, :, reference_z]

    # Rotate 90° for display; with origin='upper', row index 0 is top.
    axial_rot = ndimage.rotate(axial_base, 90)
    ax_ref.imshow(axial_rot, cmap=cmap, vmin=vmin, vmax=vmax, origin='upper')

    # Map array y index to display row so that larger y (more anterior) draws higher.
    y_disp = axial_rot.shape[0] - 1 - y_coord
    ax_ref.axhline(y=y_disp, color='red', linestyle='--', linewidth=2)

    ax_ref.set_title(f"Axial ref (z={reference_z})\nred line: y={y_coord}", fontsize=9)

    if show:
        plt.show()
    else:
        return fig




